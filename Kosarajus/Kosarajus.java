	public static void DFS(int start, boolean[] visited) {
		visited[start] = true;
		int[] temp = adj[start];
		for (int i = 0; i < temp.length; i++) {
			if (!visited[i] && temp[i] != 0) {
				DFS(i, visited);
			}
		}
		stack.add(start);
	}

	public static void reverse() {
		int[][] temp = new int[adj.length][adj.length];
		for (int i = 0; i < temp.length; i++) {
			for (int l = 0; l < temp.length; l++) {
				temp[l][i] = adj[i][l];
			}
		}
		adj = temp;
	}

